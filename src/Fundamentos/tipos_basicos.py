# Tipos basicos em Python
# Inteiros
numero_inteiro = 10
# Flutuantes
numero_flutuante = 10.5
# Strings
texto = "Olá, Mundo!"
# Booleanos
verdadeiro = True
falso = False
# Listas
lista = [1, 2, 3, 4, 5]
# Tuplas
tupla = (1, 2, 3, 4, 5)
# Dicionários
dicionario = {
    "chave1": "valor1",
    "chave2": "valor2",
    "chave3": "valor3"
}
# Conjuntos
conjunto = {1, 2, 3, 4, 5}
# None
nulo = None

# Exibindo os tipos
print(f"Tipo de numero_inteiro: {type(numero_inteiro)}")
print(f"Tipo de numero_flutuante: {type(numero_flutuante)}")
print(f"Tipo de texto: {type(texto)}")
print(f"Tipo de verdadeiro: {type(verdadeiro)}")
print(f"Tipo de falso: {type(falso)}")
print(f"Tipo de lista: {type(lista)}")
print(f"Tipo de tupla: {type(tupla)}")
print(f"Tipo de dicionario: {type(dicionario)}")
print(f"Tipo de conjunto: {type(conjunto)}")
print(f"Tipo de nulo: {type(nulo)}")
