# operadores de atribuição
# Atribuição simples
a = 10
a = a + 3
print(a) # Saída: 13
# Atribuição com operadores de atribuição
a += 5  # Equivalente a: a = a + 5
print(a)  # Saída: 18
a -= 2  # Equivalente a: a = a - 2
print(a)  # Saída: 16
a *= 2  # Equivalente a: a = a * 2
print(a)  # Saída: 32
a /= 4  # Equivalente a: a = a / 4
print(a)  # Saída: 8.0
a //= 2  # Equivalente a: a = a // 2
print(a)  # Saída: 4.0
a %= 3  # Equivalente a: a = a % 3
print(a)  # Saída: 1.0
a **= 3  # Equivalente a: a = a ** 3
print(a)  # Saída: 1.0