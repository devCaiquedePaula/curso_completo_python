# Operadores Relacionais
# Operadores relacionais são usados para comparar dois valores.
# Eles retornam um valor booleano (True ou False).
print(3 > 4)  # False, pois 3 não é maior que 4
print(3 < 4)  # True, pois 3 é menor que 4
print(3 >= 4) # False, pois 3 não é maior ou igual a 4
print(3 <= 4) # True, pois 3 é menor ou igual a 4
print(3 == 4) # False, pois 3 é diferente de 4
print(3 != 4) # True, pois 3 é diferente de 4
print(3 == '3') # False, pois são tipos diferentes (int e str)