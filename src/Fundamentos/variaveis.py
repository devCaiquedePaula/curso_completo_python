# Variaveis são espaços na memória onde podemos armazenar dados.
# Em Python, as variáveis são dinamicamente tipadas, o que significa que você não precisa declarar o tipo da variável explicitamente.
# Aqui estão alguns exemplos de variáveis em Python:

a = 10  # Inteiro
b = 3.14  # Flutuante
print(a + b)  # Soma de inteiro e flutuante

a = "Agora sou uma string"  # String
print(a)

print(a + b)  # Isso causará um erro, pois não podemos somar uma string com um float
